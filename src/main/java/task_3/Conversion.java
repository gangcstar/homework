package task_3;

public class Conversion {
    public static double convertFahrenheit(double value) {          // конвертация в градусы Цельсия
        return value*1.8+32;
    }
    public static double convertKelvin(double value) {          // конвертация в градусы Кельвина
        return value+273.15;
    }
}
